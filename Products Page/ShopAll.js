mycontainer = document.getElementById("products_container")



function append(data){

    mycontainer.innerHTML = null;
    productsarr.map(function(elem){
        var box = document.createElement('div');
        box.setAttribute('class','items')
        
    
        const button = document.createElement('button');
        button.innerText = 'ADD TO CART';
        button.setAttribute('class','addtocart')
        button.addEventListener("click", function(){
            cartfun(elem)
        })
    
        var img = document.createElement('img');
        img.setAttribute('class','itemsimg')
        img.src=elem.img_url;
        img.addEventListener("click", function(){
            productpage(elem)
        })
        
        var name = document.createElement('h5');
        name.innerText=elem.name;
    
        var description = document.createElement('p')
        description.innerText=elem.description;
        description.setAttribute('class','descritpion')
    
        let costdiv = document.createElement('div')
         costdiv.setAttribute('class','costdiv')
                
         let cost = document.createElement('h4')
         cost.innerText = `₹${elem.cost}`;
         //console.log(typeof(elem.cost))
    
        let strikecost = document.createElement('h5')
        strikecost.innerText = elem.strikecost;
         strikecost.setAttribute('class','strikecosts')
    
        let bestsellimg = document.createElement('img')
        bestsellimg.src = elem.bestsellimg;
        bestsellimg.setAttribute('class','bestsellimg')
    
         costdiv.append(cost,strikecost);
        box.append(img,name,description,costdiv,button,bestsellimg)
    
    
    
        mycontainer.append(box)
       
    })

}

