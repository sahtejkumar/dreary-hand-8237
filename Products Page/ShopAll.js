document.getElementById('pro_select').addEventListener("change", changefunction);

function changefunction(){
    //console.log("hi");
   let value = document.getElementById('pro_select').value;
   console.log(value);
    
   if(value == 'low'){
    productsarr.sort(function(a,b){
        return +a.cost-+b.cost
    })
   }   

   if(value == 'high'){
    productsarr.sort(function(a,b){
        return +b.cost-+a.cost
    })
   }   

   if(value == 'a-z'){
    productsarr.sort(function(a,b){
        if(a.name>b.name) {return 1}
        if(a.name<b.name) {return -1}
        if(a.name==b.name) {return 0}
    })
   }
   if(value == 'z-a'){
    productsarr.sort(function(a,b){
        if(a.name>b.name) {return -1}
        if(a.name<b.name) {return 1}
        if(a.name==b.name) {return 0}
    })
   }   
   append(productsarr)
}